package main.com.java.model;

public class Course {
    private String teachingClass;//教学班
    private String courseIdAndCourseName;//教学课程
    private String courseCategory;//类别
    private String classClassification;//教学班分类
    private String courseSelectionState;//选课状态
    private String courseTeacher;//上课教师
    private String courseAddress;//地点
    private String courseTime;//上课时间
    private String courseStudent;//学生名单
    private String remark;//备注

    public String getTeachingClass() {
        return teachingClass;
    }

    public void setTeachingClass(String teachingClass) {
        this.teachingClass = teachingClass;
    }

    public String getCourseIdAndCourseName() {
        return courseIdAndCourseName;
    }

    public void setCourseIdAndCourseName(String courseIdAndCourseName) {
        this.courseIdAndCourseName = courseIdAndCourseName;
    }

    public String getCourseCategory() {
        return courseCategory;
    }

    public void setCourseCategory(String courseCategory) {
        this.courseCategory = courseCategory;
    }

    public String getClassClassification() {
        return classClassification;
    }

    public void setClassClassification(String classClassification) {
        this.classClassification = classClassification;
    }

    public String getCourseSelectionState() {
        return courseSelectionState;
    }

    public void setCourseSelectionState(String courseSelectionState) {
        this.courseSelectionState = courseSelectionState;
    }

    public String getCourseTeacher() {
        return courseTeacher;
    }

    public void setCourseTeacher(String courseTeacher) {
        this.courseTeacher = courseTeacher;
    }

    public String getCourseAddress() {
        return courseAddress;
    }

    public void setCourseAddress(String courseAddress) {
        this.courseAddress = courseAddress;
    }

    public String getCourseTime() {
        return courseTime;
    }

    public void setCourseTime(String courseTime) {
        this.courseTime = courseTime;
    }

    public String getCourseStudent() {
        return courseStudent;
    }

    public void setCourseStudent(String courseStudent) {
        this.courseStudent = courseStudent;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }
}
